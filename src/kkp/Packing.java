    private void tablePackingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePackingMouseClicked
        int rowPoint = tablePacking.rowAtPoint(evt.getPoint());

        this.idPacking = tablePacking.getValueAt(rowPoint, 0).toString();

        String getPackingName = tablePacking.getValueAt(rowPoint, 1).toString();
        inputPackingName.setText(getPackingName);

        String getPrice = tablePacking.getValueAt(rowPoint, 2).toString();
        inputPrice.setText(getPrice);
    }//GEN-LAST:event_tablePackingMouseClicked

