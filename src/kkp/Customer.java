    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked
        int rowPoint = tableCustomer.rowAtPoint(evt.getPoint());

        this.idCustomer = tableCustomer.getValueAt(rowPoint, 0).toString();

        String getFullname = tableCustomer.getValueAt(rowPoint, 1).toString();
        inputFullname.setText(getFullname);
        
        String getAddress = tableCustomer.getValueAt(rowPoint, 2).toString();
        inputAddress.setText(getAddress);

        String getEmail = tableCustomer.getValueAt(rowPoint, 3).toString();
        inputEmail.setText(getEmail);

        String getPhone = tableCustomer.getValueAt(rowPoint, 4).toString();
        inputPhone.setText(getPhone);
    }//GEN-LAST:event_tableCustomerMouseClicked

